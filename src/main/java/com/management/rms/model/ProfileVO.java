package com.management.rms.model;

public class ProfileVO {
	
	private String ResumeFileAddress;
	
	private String Skills;
	
	private String Education;
	
	private String Experience;
	
	private String Name;
	
	private String Email;
	
	private String Phone;



	public String getResumeFileAddress() {
		return ResumeFileAddress;
	}

	public void setResumeFileAddress(String resumeFileAddress) {
		ResumeFileAddress = resumeFileAddress;
	}

	public String getSkills() {
		return Skills;
	}

	public void setSkills(String skills) {
		Skills = skills;
	}

	public String getEducation() {
		return Education;
	}

	public void setEducation(String education) {
		Education = education;
	}

	public String getExperience() {
		return Experience;
	}

	public void setExperience(String experience) {
		Experience = experience;
	}

	public String getName() {
		return Name;
	}

	public void setName(String name) {
		Name = name;
	}

	public String getEmail() {
		return Email;
	}

	public void setEmail(String email) {
		Email = email;
	}

	public String getPhone() {
		return Phone;
	}

	public void setPhone(String phone) {
		Phone = phone;
	}

	
}
